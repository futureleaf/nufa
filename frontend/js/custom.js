$(function() {
// OPACITY OF BUTTON SET TO 0%
$(".roll").css("opacity","0");
$(".roll").css("background-color","rgb(134,196,64)");
 
// ON MOUSE OVER
$(".roll").hover(function () {
 
// SET OPACITY TO 70%
$(this).stop().animate({
opacity: .9
}, "slow");
},
 
// ON MOUSE OUT
function () {
 
// SET OPACITY BACK TO 50%
$(this).stop().animate({
opacity: 0
}, "slow");
});
});

